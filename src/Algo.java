public class Algo {

		public boolean evenTest (int n) {
			if (n%2==0) {
				
				return true;
			
			}
			else;
				return false;
		
	}

public static void main (String [] args){
	
	Algo a= new Algo();
	boolean result=a.evenTest(5);
	if (result) {
		System.out.println("number is even");
	}
	else {
		System.out.println("number is odd");
		
	}
	
	}
}
